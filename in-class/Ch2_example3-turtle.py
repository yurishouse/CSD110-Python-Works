# Turtle graphics

# need access to the library to use function
import turtle

murtle = turtle.Turtle()

# change color to blue
murtle.pencolor("blue")
# change the line thickness
murtle.pensize(4)
# move the turtle 200 pixels
murtle.forward(200)
murtle.right(90) # Turn right 90 degree
murtle.forward(200)
murtle.right(90) # Turn right 90 degree
murtle.forward(200)
murtle.right(90) # Turn right 90 degree
murtle.forward(200)

murtle.circle(60)

turtle.done()
